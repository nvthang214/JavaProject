package Sort;

import java.util.Random;

/**
 *
 * @author emlad
 */
public class GetArray {

    public int[] getArray(int n) {
        int[] array = new int[n];
        Random random = new Random();
        for (int i = 0; i < n; i++) {
            array[i] = random.nextInt(n);
        }
        return array;
    }
}
